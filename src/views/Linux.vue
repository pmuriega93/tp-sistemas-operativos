        <h1>¿Por qué usar Linux?</h1>
        <h2>Linux es para muchas personas un sistema un poco complicado de usar, incluso puede ser tachado como algo "extraño". Quizás esto tenga que ver con el hecho de que todos hemos crecido utilizando Windows en todos los ámbitos de nuestra vida. Veremos en esta entrada por qué deberíamos considerar darle una oportunidad a Linux en nuestras computadoras.</h2>

