            <h4>Software libre</h4>
            <p>
                A diferencia de Microsoft y Windows, detrás de Linux no es habitual encontrarnos con una empresa con intereses empresariales, de manera que es más fácil evitar problemas de tipo legal o violaciones de nuestra privacidad o seguridad por parte de quienes han programado esa aplicación o versión de Linux que usamos. Un ejemplo es la recopilación de datos de uso. A diferencia de los sistemas operativos comerciales, en Linux no es habitual toparse con este problema.
                Este tema fue tratado en <a href="/software-libre">esta entrada</a>, donde tratamos más extensamente el tema del software libre. Si te interesa esta filosofía de software vas a encontrarte a gusto con Linux. Como el código fuente de Linux siempre está disponible, no hay problemas de datos o privacidad. Linux le permite monitorear virtualmente todo lo que transmite su sistema si es lo suficientemente experto en tecnología. Decenas de miles de ojos están revisando el código fuente en busca de errores que puedan exponer alguna vulnerabilidad en el sistema. Por lo tanto, Linux es el sistema operativo elegido por aquellos que valoran su privacidad.
            </p>
 
