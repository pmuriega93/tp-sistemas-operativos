            <h3>Ventajas de usar Linux</h3>
            <p>
                Este sistema operativo es  gratuito y de código abierto. Para usarlo no es necesario pagar ninguna licencia o tasa, da igual si lo usamos en un ordenador o en un servidor a gran escala. Si sabemos configurarlo y gestionarlo, ahorraremos una buena cantidad de dinero. En caso de que necesitemos soporte profesional, siempre podemos recurrir a sistemas como Red Hat Enterprise Linux. Linux es un sistema operativo sin restricciones. Los usuarios tienen control total sobre él, pudiendo modificarlo según sus necesidades y ajustarlo para que sea lo más parecido posible al sistema real donde se va a ejecutar. Además, tienen control total sobre los paquetes y programas que se ejecutan, pudiendo dejar el sistema justo con lo necesario para poder realizar su trabajo. Podemos usarlo con un escritorio, o desde una terminal para aprovechar al máximo los recursos.<br>
                Este sistema operativo se puede instalar en cualquier ordenador, aunque sea antiguo y disponga de pocos recursos. Las actualizaciones también son muy sencillas y rápidas, pudiendo configurarlas tanto de forma automática como manual. Pero de cualquier manera, no reiniciarán furtivamente la computadora como lo hace Windows. La estabilidad y seguridad  frente a otros sistemas, como Windows, también es una ventana que los desarrolladores tienen en cuenta. Por último, Linux dispone de una  gran cantidad de herramientas de programación, pudiendo utilizar la que queramos en función del tipo de software que vayamos a crear. Además, gracias a su fácil personalización, es posible tener varias configuraciones (con diferentes compiladores, por ejemplo) para poder probar el comportamiento del programa bajo diferentes plataformas en un mismo sistema. También es importante señalar que Linux cuenta con una gran comunidad de soporte, y que, dado que la mayoría de programadores también utilizan Linux, la documentación que encontraremos para realizar determinadas tareas está enfocada principalmente a este SO.<br>
                Linux es seguro y sigue la filosofía de los sistemas Unix; su robusto sistema de permisos y usuarios no permite a los mismos borrar ni instalar programas, ni tocar archivos del sistema. Esto sólo lo puede hacer el superusuario o 'root', que es el único que tiene los permisos adecuados para hacerlo. En un ordenador personal el root y el usuario suelen ser la misma persona, pero en Linux se emplean distintas cuentas de usuario para cada perfil. Así no se producirán los mismos problemas que se dan, por ejemplo, en Windows, que te deja borrar, por ejemplo, la carpeta 'Archivos de Programa' o los archivos: User.dat o System.dat, momento en el que el ordenador dejará de funcionar y tendrás que instalar todos tus programas de nuevo, además del propio Windows.<br>
                Si el sistema operativo que usas es Windows, conocerás sobradamente sus pantallazos azules o BSOD (Blue Screen of Death). Linux no tiene estos problemas, puesto que cada programa trabaja de manera independiente, lo que hace que si una parte del sistema falla no falle el resto. También es muy usual que sea necesario reinstalar Windows de vez en cuando porque empiezas a ver que no funciona adecuadamente, esto se debe a que en Windows las librerías que vienen por defecto son muy escasas. Por este motivo los programas instalan sus propias librerías (.dll), sin preocuparse si ya existían o no, por lo que al desinstalar el programa no las quita, esto hace que tu ordenador vaya cada vez más lento según se van instalando o desinstalando determinados programas. Por el contrario Linux lleva un control de librerías y programas con el sistema de paquetes, abiendo en todo momento que es cada librería, quien la utiliza y si sigue siendo necesaria.<br>
                La velocidad a la que se hace la corrección de los errores de los programas en Linux es muy alta, puesto que en el desarrollo de Linux colaboran muchas personas en todo el mundo. Cualquier persona que tenga el conocimiento suficiente puede detectar los errores de los programas y corregirlos, o bien mandarle un mail al programador para que éste lo corrija, puesto que Linux siempre ha sido de código abierto, libre distribución y gratuito (aunque también existen distribuciones de Linux no gratuitas como la poderosa y conocida distribución RedHat para grandes servidores).
            </p>
            <h4>Facilidad de uso</h4>
            <p>
                Una de las barreras que durante años ha evitado a muchos usar Linux es su complejidad. O al menos lo era cuando la mayoría de tareas debías hacerlas desde la línea de comandos. En la actualidad, distribuciones Linux como Ubuntu, Mint, Manjaro, Debian u OpenSUSE ofrecen una interfaz similar a Windows y con todas las herramientas y aplicaciones necesarias para empezar a disfrutar desde el primer día. Si necesitas nuevo software, la mayoría de distros cuentan con su propia tienda de aplicaciones o herramienta de gestión de apps. Todo está pensado para que cualquiera pueda manejarse sin problemas. Está claro que existen versiones de Linux complejas, pero están enfocadas a un público muy concreto. Las distros domésticas cumplen con creces con los requisitos de usuarios amateurs o recién llegados.
            </p>
            <h4>Software y hardware compatibles</h4>
            <p>
                Hoy en día la mayoría de aplicaciones y servicios online cuentan con versión compatible para cualquier sistema operativo. Actualmente es más fácil crear una aplicación multiplataforma, por lo que Linux cuenta con un catálogo de software que poco o nada tiene que envidiar a Windows o macOS. En el catálogo destacan las aplicaciones gratuitas y de código abierto, pero también surgen proyectos comerciales, y en la lista se incluyen los juegos, cada vez más presentes en Linux. Seguramente hay algún software no disponible en Linux, pero es más que probable que encontremos una alternativa o, en su defecto, que podamos ejecutarlo mediante Wine o empleando máquinas virtuales como VirtualBox. En cuanto al hardware, la comunidad Linux ha avanzado mucho en la creación de controladores o drivers para emplear cualquier dispositivo o componente en Linux. Podemos encontrarnos con alguna excepción, pero la mayoría de dispositivos cuentan con controlador compatible por defecto.
            </p>
            <h4>Variedades a gusto del usuario</h4>
            <p>
                Linux cuenta con diferentes escritorios y gestores de ventanas, de manera que podemos elegir el que queramos, algo que permiten muchas distribuciones Linux. Mientras que Windows cuenta con un escritorio por defecto, en Linux podemos elegir GNOME, KDE, Mate, Cinnamon, LXDE, XFCE… En la variedad está el gusto. Además, cualquier configuración o elemento del sistema operativo es susceptible de ser alterado. La única limitación es que seamos capaces o tengamos los conocimientos adecuados. Pero siempre podemos encontrar en internet un tutorial donde nos explique como hacerlo.
            </p>
