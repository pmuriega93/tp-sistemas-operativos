            <h3>A modo de definición</h3>
            <p>
                Su definición está asociada al nacimiento del movimiento del software libre, encabezado por Richard Stallman y la fundación de la Free Software Foundation (Fundación por el Software Libre) en 1985. El software libre es todo aquel software cuyo código fuente se puede estudiar, modificar y utilizar libremente con cualquier fin, e incluso, copiar y redistribuir el programa con cambios o sin ellos. En otras palabras, este tipo de software le da la libertad a cualquier persona u organización de usar un programa para cualquier clase de trabajo y sin la obligación de comunicárselo a un desarrollador o a alguna entidad específica.
                El software libre se puede definir en contraposición a lo que significa el código propietario, a saber: código oculto y con derechos restringidos, sin dar posibilidad de realizar ninguna adaptación ni cambios que no haya realizado previamente la empresa fabricante. 
                Un programa es software libre si los usuarios tienen las cuatro libertades esenciales:
            </p>
                <ul class="freedom-list">
                    <li class="freedom-item">Libertad 0: de usar el programa con cualquier propósito<br><p>Al usuario no se le prohíbe o se le impide ejecutarlo ya que lo que importa es su propósito y no el del programador.</p></li>
                    <li class="freedom-item">Libertad 1: de estudiar cómo funciona el programa y poder modificarlo<br><p>Constituye la libertad de usar la versión modificada (según los deseos y necesidades del usuario) en lugar de la original.</p></li>
                    <li class="freedom-item">Libertad 2: de distribuir copias del programa a cualquier persona u organización<br><p>Los usuarios tienen la libertad de redistribuir y compartir copias con o sin modificaciones, sin necesidad de pedir ni pagar algún permiso para hacerlo. Además, se puede distribuir los programas gratuitamente o cobrando alguna tarifa por ello.</p></li>
                    <li class="freedom-item">Libertad 3: de mejorar el programa y compartir las mejoras en beneficio de todos<br><p>Permite mejorar el programa — y, al igual que la libertad 2 — posibilita compartir y liberar las versiones modificadas como software libre y, de tal forma, beneficiar con ello a las personas u organizaciones que lo utilicen.</p></li>
                </ul>
                <p>
                Si falta alguna de ellas, o si son inadecuadas, el programa es privativo (no es libre) e injusto. Es decir, que un software se puede clasificar como ‘libre’ si los usuarios disponen de estas cuatro libertades, cuya descripción se plasma en la licencia GPL GNU (GNU General Public License) que se otorga a un programa informática cuando cumple estos requisitos.

                Para que estas libertades sean reales, en GNU sostienen que deben ser permanentes e irrevocables: Si el programador tiene el poder de revocar la licencia, o añadir restricciones a las condiciones de uso en forma retroactiva, sin acciones del usuario que lo justifiquen, el software no es libre. Si una licencia requiere que se cumpla con las licencias de todos los programas que usa el usuario, y si él ejecuta programas que no son libres, este requisito implicaría cumplir con las licencias de esos programas privativos, lo cual hace que la licencia no sea libre. No obstante, es aceptable que una licencia especifique la jurisdicción de competencia o la sede para la resolución de conflictos, o ambas cosas. Por otra parte, ciertos tipos de reglas sobre el modo de distribuir software libre son aceptables, cuando no entran en conflicto con las libertades principales. Por ejemplo el copyleft , la regla en base a la cual, cuando redistribuye el programa, no se puede agregar restricciones para denegar a los demás las libertades principales. En lugar de entrar en conflicto con las libertades principales, las protege. De todos modos, el software libre sin copyleft también es ético. </p>

            <h3>Las ventajas de usar software libre</h3>
            <p>
                Habiendo definido el tema, vamos a pasar a analizar las diferentes ventajas que pueden obtenerse del uso de software libre. <br>
            </p>
            <h4>Compatibilidad</h4>
            <p>
                Una de las grandes diferencias que existen con respecto al software privativo es la compatibilidad al adquirir hardware nuevo. El software privativo suele trabajar en círculo con la necesidad de ir modernizando el hardware en donde se ejecuta. Provocando que a medida que cambian las versiones de los programas haya una necesidad de modernizar el hardware porque el mismo ya no es compatible. Esto está pensado desde la idea básica que promueve el modelo de la obsolescencia programada. Cuando hablamos de software libre esta idea es totalmente diferente, en estos ámbitos la idea es que las aplicaciones funcionen en cualquier hardware y que funcione bien. Se intenta evitar el cambio de hardware solo porque una aplicación cambie en su versión. Además es mucho más barato económicamente afrontar  una instalación o actualización de estándares abiertos que privados ya que son completamente gratuitos. Por último, al tener acceso al código fuente de los programas, permite la traducción de una aplicación a otros idiomas, respetando la diversidad cultural de cada territorio.
            </p>
            <h4>Soporte técnico</h4>
            <p>
                El software libre tiene una pata muy importante en la comunidad que está detrás de ella. Son los colaboradores quienes aportan tanto programando como cumpliendo diferentes roles en comunidad para aportar al software libre. Entre esos roles está obviamente el soporte técnico a otros usuarios, y esto evita cierta dependencia que existe en el software privativo, ya que al ser parte de una empresa dicho software siempre genera una supeditación hacia dicha empresa en cuanto al soporte técnico.
            </p>
            <h4>Seguridad</h4>
            <p>
                Contar con el código fuente de una aplicación es una gran ventaja que por supuesto aquellas aplicaciones que no permiten acceder a dicho código fuente no pueden brindar. El software privativo muestra este inconveniente ya que protege su código fuente para que nadie pueda acceder. Generando un gran problema de seguridad. No solo porque no hay forma de saber qué es lo que va a hacer la aplicación, sino porque dependemos de quien es el dueño en casos de encontrarnos con algún error en la aplicación. 
            </p>
            <h4>Corrección de errores</h4>
            <p>
                El software de fuentes abiertas ofrece transparencia sobre el funcionamiento de los programas, favoreciendo que siempre se pueda retomar el desarrollo y actualización de  aplicaciones.
                Al encontrar un error en una aplicación de software privativo, el usuario no puede hacer más que enviar un reporte de dicho error y ahí queda toda la situación. Ya que por más que quiera no tiene acceso al código fuente. En el ámbito del software libre eso es totalmente diferente, se dispone del código fuente. Y más allá de la capacidad de solucionar un error o no si se dispone del código fuente se puede llegar a reportar un error, e incluso se podría llegar a aportar una solución al mismo. 
                <br>
            </p>
